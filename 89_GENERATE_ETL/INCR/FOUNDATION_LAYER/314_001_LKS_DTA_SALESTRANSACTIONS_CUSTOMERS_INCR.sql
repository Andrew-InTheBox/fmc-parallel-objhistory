			, "STG_TEMP_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
			, TO_TIMESTAMP(NULL, 'DD/MM/YYYY HH24:MI:SS') AS "LOAD_END_DATE"
			, "STG_TEMP_SRC"."RECORD_TYPE" AS "RECORD_TYPE"
			, 'STG' AS "SOURCE"
			, 1 AS "ORIGIN_ID"
			, CASE WHEN "STG_TEMP_SRC"."__$operation" = 'D' THEN CAST('Y' AS VARCHAR(3)) ELSE CAST('N' AS VARCHAR(3)) END AS "DELETE_FLAG"
			, "STG_TEMP_SRC"."CDC_SIMULATED" AS "CDC_SIMULATED"
			, "STG_TEMP_SRC"."TRANSACTION_ID" AS "TRANSACTION_ID"
			, "STG_TEMP_SRC"."CUSTOMER_NAME" AS "CUSTOMER_NAME"
		FROM "TEST_FMC_STG"."SALES_TRANSACTIONS" "STG_TEMP_SRC"
		UNION 
		SELECT 
			  "LKS_SRC"."LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY" AS "LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY"
			, "LNK_SRC"."CUSTOMERS_HKEY" AS "CUSTOMERS_HKEY"
			, "LNK_SRC"."SALES_TRANSACTIONS_HKEY" AS "SALES_TRANSACTIONS_HKEY"
			, "LKS_SRC"."LOAD_DATE" AS "LOAD_DATE"
			, "LKS_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
			, MAX("LKS_SRC"."LOAD_DATE")OVER(PARTITION BY "LNK_SRC"."SALES_TRANSACTIONS_HKEY") AS "LOAD_END_DATE"
			, 'SAT' AS "RECORD_TYPE"
			, 'LKS' AS "SOURCE"
			, 0 AS "ORIGIN_ID"
			, "LKS_SRC"."DELETE_FLAG" AS "DELETE_FLAG"
			, "LKS_SRC"."CDC_SIMULATED" AS "CDC_SIMULATED"
			, "LKS_SRC"."TRANSACTION_ID" AS "TRANSACTION_ID"
			, "LKS_SRC"."CUSTOMER_NAME" AS "CUSTOMER_NAME"
		FROM "DV_FL"."LKS_DTA_SALESTRANSACTIONS_CUSTOMERS" "LKS_SRC"
		INNER JOIN "DV_FL"."LNK_SALESTRANSACTIONS_CUSTOMERS" "LNK_SRC" ON  "LKS_SRC"."LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY" = "LNK_SRC"."LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY"
		INNER JOIN "DIST_STG" "DIST_STG" ON  "LNK_SRC"."SALES_TRANSACTIONS_HKEY" = "DIST_STG"."SALES_TRANSACTIONS_HKEY"
	)
	SELECT 
		  "TEMP_TABLE_SET"."LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY" AS "LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY"
		, "TEMP_TABLE_SET"."CUSTOMERS_HKEY" AS "CUSTOMERS_HKEY"
		, "TEMP_TABLE_SET"."SALES_TRANSACTIONS_HKEY" AS "SALES_TRANSACTIONS_HKEY"
		, "TEMP_TABLE_SET"."LOAD_DATE" AS "LOAD_DATE"
		, "TEMP_TABLE_SET"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
		, "TEMP_TABLE_SET"."RECORD_TYPE" AS "RECORD_TYPE"
		, "TEMP_TABLE_SET"."SOURCE" AS "SOURCE"
		, CASE WHEN "TEMP_TABLE_SET"."SOURCE" = 'STG' AND "TEMP_TABLE_SET"."DELETE_FLAG"::varchar || "TEMP_TABLE_SET"."CUSTOMERS_HKEY"::varchar =
			LAG( "TEMP_TABLE_SET"."DELETE_FLAG"::varchar || "TEMP_TABLE_SET"."CUSTOMERS_HKEY"::varchar,1)OVER(PARTITION BY "TEMP_TABLE_SET"."SALES_TRANSACTIONS_HKEY" ORDER BY "TEMP_TABLE_SET"."LOAD_DATE","TEMP_TABLE_SET"."ORIGIN_ID")THEN 1 ELSE 0 END AS "EQUAL"
		, "TEMP_TABLE_SET"."DELETE_FLAG" AS "DELETE_FLAG"
		, "TEMP_TABLE_SET"."CDC_SIMULATED" AS "CDC_SIMULATED"
		, "TEMP_TABLE_SET"."TRANSACTION_ID" AS "TRANSACTION_ID"
		, "TEMP_TABLE_SET"."CUSTOMER_NAME" AS "CUSTOMER_NAME"
	FROM "TEMP_TABLE_SET" "TEMP_TABLE_SET"
	WHERE  "TEMP_TABLE_SET"."SOURCE" = 'STG' OR("TEMP_TABLE_SET"."LOAD_DATE" = "TEMP_TABLE_SET"."LOAD_END_DATE" AND "TEMP_TABLE_SET"."SOURCE" = 'LKS')
	;
`} ).execute();

