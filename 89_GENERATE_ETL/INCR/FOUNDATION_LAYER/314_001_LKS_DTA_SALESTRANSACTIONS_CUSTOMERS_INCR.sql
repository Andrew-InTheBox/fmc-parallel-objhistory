	INSERT INTO "TEST_FMC_STG"."LKS_DTA_SALESTRANSACTIONS_CUSTOMERS_TMP"(
		 "LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY"
		,"CUSTOMERS_HKEY"
		,"SALES_TRANSACTIONS_HKEY"
		,"LOAD_DATE"
		,"LOAD_CYCLE_ID"
		,"RECORD_TYPE"
		,"SOURCE"
		,"EQUAL"
		,"DELETE_FLAG"
		,"CDC_SIMULATED"
		,"TRANSACTION_ID"
		,"CUSTOMER_NAME"
	)
	WITH "DIST_STG" AS 
	( 
		SELECT 
			  "STG_DIS_SRC"."SALES_TRANSACTIONS_HKEY" AS "SALES_TRANSACTIONS_HKEY"
			, "STG_DIS_SRC"."LOAD_CYCLE_ID" AS "LOAD_CYCLE_ID"
			, MIN("STG_DIS_SRC"."LOAD_DATE") AS "MIN_LOAD_TIMESTAMP"
		FROM "TEST_FMC_STG"."SALES_TRANSACTIONS" "STG_DIS_SRC"
		GROUP BY  "STG_DIS_SRC"."SALES_TRANSACTIONS_HKEY",  "STG_DIS_SRC"."LOAD_CYCLE_ID"
	)
	, "TEMP_TABLE_SET" AS 
	( 
		SELECT 
			  "STG_TEMP_SRC"."LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY" AS "LNK_SALESTRANSACTIONS_CUSTOMERS_HKEY"
			, "STG_TEMP_SRC"."CUSTOMERS_HKEY" AS "CUSTOMERS_HKEY"
			, "STG_TEMP_SRC"."SALES_TRANSACTIONS_HKEY" AS "SALES_TRANSACTIONS_HKEY"
